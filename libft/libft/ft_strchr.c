#include "../libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = ft_strlen(str) + 1;
	while (c > 256)
		c -= 256;
	while (i--)
		if (*str++ == c)
			return ((char *)--str);
	return (0);
}
